  @override
  void initState() {
    super.initState();
    _model = createModel(context, () => LoginModel());

    _model.emailAddressController ??= TextEditingController();
    _model.passwordController ??= TextEditingController();
    setupAnimations(
      animationsMap.values.where((anim) =>
          anim.trigger == AnimationTrigger.onActionTrigger ||
          !anim.applyInitialState),
      this,
    );

    WidgetsBinding.instance.addPostFrameCallback((_) => setState(() {}));
  }

  void _handleLogin() async {
    final username = _model.emailAddressController.text;
    final password = _model.passwordController.text;

    // Verificar las credenciales usando la instancia global de DatabaseController
    final isValidCredentials = await Config.databaseController
        .verificarCredenciales(username, password);

    if (isValidCredentials) {
      // Navegar a la pantalla principal
      Navigator.pushNamed(context, '/home');
    } else {
      // Mostrar un mensaje de error
      ScaffoldMessenger.of(context).showSnackBar(
        SnackBar(
          content: Text('Credenciales inválidas'),
          duration: Duration(seconds: 3),
        ),
      );
    }
  }

  @override
  void dispose() {
